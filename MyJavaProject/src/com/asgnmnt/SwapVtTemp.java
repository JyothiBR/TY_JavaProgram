package com.asgnmnt;

public class SwapVtTemp 
{
	public static void main(String[] args) 
	{
		int a=2;
		int b=4;
		int temp;
		System.out.println("Before Swapping :");
		System.out.println(a);
		System.out.println(b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After Swapping :");
		System.out.println(a);
		System.out.println(b);
	}
}
